      validator: (value) =>
        [
          '',
          'blue',
          'light-blue',
          'purple',
          'indigo',
          'deep-blue',
          'cyan',
          'mint',
          'green',
          'lime',
          'yellow',
          'orange',
          'red',
          'dark-gray',
          'cool-gray',
          'white',
          'black',
        ].includes(value) || value.startsWith('#'),
    },
  },
  computed: {
    colorCode() {
      if (this.color.startsWith('#')) {
        return {
          fill: this.color,
        };
      }
      return {};
    },
    sizeStyle() {
      if (this.customSize) {
        return {
          width: `${this.customSize}px`,
          height: `${this.customSize}px`,
        };
      }
      return {};
    },
  },
};
